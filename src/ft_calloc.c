#include "libft.h"

void	*ft_calloc(size_t elmnt_count, size_t elmnt_size)
{
	void	*ptr;
	size_t	total_size;

	total_size = elmnt_count * elmnt_size;
	if (elmnt_size != 0 && total_size / elmnt_size != elmnt_count)
		return (NULL);
	ptr = malloc(total_size);
	if (!ptr)
		return (NULL);
	ft_memset(ptr, 0, total_size);
	return (ptr);
}
